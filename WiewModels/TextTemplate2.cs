RelayCommand — класс для команд
Что это:
RelayCommand — это реализация интерфейса ICommand. Он позволяет привязать метод из ViewModel к кнопке в XAML.

Зачем нужен:
Без RelayCommand обработку клика по кнопке пришлось бы писать в code-behind (MainWindow.xaml.cs), что нарушает MVVM. ViewModel не должна знать о кнопках, а кнопка не должна напрямую вызывать метод ViewModel.

RelayCommand — это "мост": кнопка вызывает команду, команда вызывает метод в ViewModel.

Почему в нашем проекте:
Мы хотим, чтобы логика (что происходит при нажатии Play) была в ViewModel, а не в MainWindow.xaml.cs. Это позволяет:

Тестировать логику без открытия окна

Менять интерфейс, не трогая код

Использовать одну команду из нескольких мест